import math

# Calculate the square root of a number
sqrt_value = math.sqrt(16)
print(f"Square root of 16 is {sqrt_value}")

# Calculate the sine of a number (in radians)
sine_value = math.sin(math.pi / 2)
print(f"Sine of pi/2 is {sine_value}")

# Calculate the cosine of a number (in radians)
cosine_value = math.cos(0)
print(f"Cosine of 0 is {cosine_value}")

# Calculate the factorial of a number
factorial_value = math.factorial(5)
print(f"Factorial of 5 is {factorial_value}")

# Calculate the greatest common divisor of two numbers
gcd_value = math.gcd(54, 24)
print(f"Greatest Common Divisor of 54 and 24 is {gcd_value}")
